export const filterRegions = (input: string): SelectGroup[] => {
  const q = input.toLowerCase().trim();
  if (!q) return regions;
  return regions
    .map((group) => ({
      ...group,
      options: group.options.filter((o) => o.label.toLowerCase().includes(q)),
    }))
    .filter((group) => group.options.length > 0);
};
